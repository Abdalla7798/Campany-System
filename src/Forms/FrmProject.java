    private String getMyLike(){
        return " like '%"+txtsearch.getText()+"%'";
    }
    
    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
        // TODO add your handling code here:
        String strSearch = "select * from project_data where ";
        boolean check = false;
   
          if (txtsearch.getText().equals("")){
           Tools.msgBox("The Search Text is empty..."); 
        }
        else{           
             if (rdonumber.isSelected()){
                 if (txtsearch.getText().matches("[0-9]+")){
                     strSearch+= "PROJECT_NO" + getMyLike();
                     check = true;
                 }
                 else {
                    Tools.msgBox("The Search Text must contain numbers only..."); 
                 }
             }
             else if (rdoname.isSelected())  {
                 if (txtsearch.getText().matches("[0-9|a-zA-Z]+")){
                     strSearch+= "PROJECT_NAME" + getMyLike();
                     check= true;
                 }
                 else {
                    Tools.msgBox("The Search Text must be numbers or Letters only..."); 
                 }
             }
             else if (rdodept.isSelected()) {
             if (txtsearch.getText().matches("[0-9|a-zA-Z]+")){
                 strSearch+= "DEARTMENT_NO"+ getMyLike() + "or DEPARTMENT" + getMyLike();
                 check = true;
             }
             else{
                 Tools.msgBox("The Search Text contain numbers or letters only...");
             }
         }
             else{
                 if (txtsearch.getText().matches("[a-zA-Z]+")){
                     strSearch+= "LOCATION"+ getMyLike();
                     check= true;
                 }
                 else {
                    Tools.msgBox("The Search Text must contain letters only...");
                 }
             }
             if (check == true){
               pro.getCustomRows(strSearch, tblproj);
             }
        }

        
    }//GEN-LAST:event_btnsearchActionPerformed

