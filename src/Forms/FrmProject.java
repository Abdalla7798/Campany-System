    Project pro = new Project();
    
    private void btnadddeptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnadddeptActionPerformed
        // TODO add your handling code here:
        int DeptNo = Integer.parseInt(dept.getAutoNumber());
        String DeptName = (String)Tools.InputBox("Enter Department Name:");
        String Location = (String)Tools.InputBox("Enter Location:");
        if (DeptName != null && Location != null){
            if (DeptName.matches("[a-zA-Z][0-9|a-zA-Z|\\s]+")){
                if (Location.matches("[a-zA-Z]+")){
                    dept.setDeptNo(DeptNo);
                    dept.setDeptName(DeptName);
                    dept.setLocation(Location);
                    dept.add();
                    db.go.fillComb("department", "deptname", cbxdept);
                }
                else{
                   Tools.msgBox("Location must be Letters only..."); 
                }
            }
            else{
                Tools.msgBox("Department Name must be numbers or Letters only but no number in the first digit...");
            }
        }
        else {
            Tools.msgBox("No Department Is Added...");
        }
    }//GEN-LAST:event_btnadddeptActionPerformed

    private void tblprojMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblprojMouseClicked
        // TODO add your handling code here:
        try{
        int row = tblproj.getSelectedRow();
        String strProjNo = tblproj.getValueAt(row, 0).toString();
        String strProjName = tblproj.getValueAt(row, 1).toString();
        String strLocation = tblproj.getValueAt(row, 2).toString();
        String strDeptName = tblproj.getValueAt(row, 4).toString();
        
        txtprojectno.setText(strProjNo);
        txtprojectname.setText(strProjName);
        txtlocation.setText(strLocation);
        cbxdept.setSelectedItem(strDeptName);
        
        btnadd.setEnabled(false);
        btnupdate.setEnabled(true);
        btndelete.setEnabled(true);
    }
    catch(Exception ex){
        Tools.msgBox(ex.getMessage());
    }
    
    }//GEN-LAST:event_tblprojMouseClicked

