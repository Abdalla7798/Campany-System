    private void setValues(){
        pro.setProjectNo(Integer.parseInt(txtprojectno.getText()));
        pro.setProjectName(txtprojectname.getText());
        pro.setLocation(txtlocation.getText());
        int deptno = Integer.parseInt(dept.getValueByName(cbxdept.getSelectedItem().toString()));
        pro.setDeptNo(deptno);
    }
    
    
    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        // TODO add your handling code here:
        
        if(txtprojectno.getText().equals("") || txtprojectname.getText().equals("") || txtlocation.getText().equals("") || cbxdept.getSelectedItem()==null){
          Tools.msgBox("The information is not Completed...");
        }
        else{
            if (txtprojectno.getText().matches("[0-9]+") && txtprojectno.getText().charAt(0)!= '0'){
                if(txtprojectname.getText().matches("[a-zA-Z][0-9|a-zA-Z|\\s]+")){
                    if (txtlocation.getText().matches("[a-zA-Z]+")){
                        setValues();
                        pro.add();
                        clearData();
                        pro.getAllRows(tblproj);
                    }
                    else{
                      Tools.msgBox("Location must be Letters only...");  
                    }
                }
                else{
                 Tools.msgBox("Project Name must be numbers or Letters only but no number in the first digit...");   
                }
            }
            else{
                Tools.msgBox("Project Number must be numbers only except zero in the first digit...");
            }
        }    
    }//GEN-LAST:event_btnaddActionPerformed

