    private String getMyLike(){
        return " like '%"+txtsearch.getText()+"%'";
    }
    
    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
        // TODO add your handling code here:
        String strSearch = "select * from workon_data where ";
              
          boolean check = false;
   
        if (txtsearch.getText().equals("")){
           Tools.msgBox("The Search Text is empty..."); 
        }
        else{           
             if (rdoEmpNo.isSelected()){
                 if (txtsearch.getText().matches("[0-9]+")){
                     strSearch+= "EMPLOYEE_NO " + getMyLike();
                     check = true;
                 }
                 else {
                    Tools.msgBox("The Search Text must be numbers only..."); 
                 }
             }
             else if (rdoEmpName.isSelected())  {
                 if (txtsearch.getText().matches("[a-zA-Z]+")){
                     strSearch+= "EMPLOYEE_NAME" + getMyLike();
                     check= true;
                 }
                 else {
                    Tools.msgBox("The Search Text must be letters only..."); 
                 }
             }
             else if (rdoProjNo.isSelected()) {
             if (txtsearch.getText().matches("[0-9]+")){
                  strSearch+= "PROJECT_NO"+ getMyLike();
                 check = true;
             }
             else{
                 Tools.msgBox("The Search Text must contain numbers only...");
             }
         }
             else{
                 if (txtsearch.getText().matches("[0-9|a-zA-Z]+")){
                     strSearch+= "PROJECT_NAME"+ getMyLike();
                     check= true;
                 }
                 else {
                    Tools.msgBox("The Search Text must be numbers or Letters only...");
                 }
             }
             if (check == true){
               work.getCustomRows(strSearch, tblworkon);
             }
        }
    }//GEN-LAST:event_btnsearchActionPerformed

