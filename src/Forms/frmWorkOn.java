    private void setValues(){
       int EmptNo = Integer.parseInt(emp.getValueByName(cbxEmp.getSelectedItem().toString())); 
       int ProjectNo = Integer.parseInt(proj.getValueByName(cbxproj.getSelectedItem().toString()));
       work.setEmpNo(EmptNo);
       work.setProjectNo(ProjectNo);
    }
    
