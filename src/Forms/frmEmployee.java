    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        // TODO add your handling code here:
        if (txtempno.getText().equals("") || txtempname.getText().equals("") || txtaddress.getText().equals("") || txtsalary.getText().equals("") || txthiringdate.getDate()==null || txtbirthdate.getDate() == null || cbxdept.getSelectedItem()==null){
        Tools.msgBox("The information is not Completed...");
        }
        else{
        Date d =new Date();
        SimpleDateFormat format =new SimpleDateFormat("yyyy-MM-dd");
        String s ="1999-12-31";
        String s2 = "1980-01-01";
        int BD = Integer.parseInt(format.format(txtbirthdate.getDate()).substring(0, 4));
        int HD = Integer.parseInt(format.format(txthiringdate.getDate()).substring(0, 4));
        Date dnow = new Date();
        Date d2 =new Date();
        Date d3 = new Date();
        try {
            d= format.parse(s);
            dnow=format.parse(format.format(d2));
            d3 = format.parse(s2);
        } catch (ParseException ex) {
            Tools.msgBox(ex.getMessage());
        }
           
            if (txtempname.getText().matches("[a-zA-Z|\\s]+")){
               if (txtempno.getText().matches("[0-9]+")&& txtempno.getText().charAt(0)!= '0'){
                   if (txtaddress.getText().matches("[a-zA-Z|\\s]+")){
                       if (txtsalary.getText().matches("[0-9]+[.][0-9]+") || txtsalary.getText().matches("[0-9]+")){
                           if(!txtbirthdate.getDate().after(d) && !txtbirthdate.getDate().before(d3)){
                               if (!txthiringdate.getDate().after(dnow) || format.format(txthiringdate.getDate()).equals(format.format(dnow))){
                                    if (HD-BD >= 20){
                                    DefaultTableModel model = (DefaultTableModel)tblphone.getModel();
                                    setValues();
                                    emp.add();
                                    Phones.setEmpNo(Integer.parseInt(txtempno.getText()));
                                    for (int x=0 ; x < tblphone.getRowCount() ; x++){
                                        Phones.setPhone(tblphone.getValueAt(x, 0).toString());
                                        Phones.add();
                                    }
                                    model.setNumRows(0);
                                    clearData();
                                    emp.getAllRows(tblEmp);
                                    }
                                    else{
                                      Tools.msgBox("The Age of employment must be at least 20 years...");
                                    }
                               }
                               else{
                                   Tools.msgBox("Hiring Date must be before today or today...");
                               }
                           }
                           else{
                             Tools.msgBox("Date Of Birth must be Max (1999) And Min (1980)");  
                           }
                       }
                       else{
                           Tools.msgBox("The Salary is wrong...");
                       }
                   }else{
                       Tools.msgBox("Address must be letters only...");
                   }
               }
                 else{
                    Tools.msgBox("The Number must be numbers only except zero in the first digit...");   
                 }
               }
               else{
                  Tools.msgBox("The Name must be letters only..."); 
               }

            }

        
    }//GEN-LAST:event_btnaddActionPerformed

