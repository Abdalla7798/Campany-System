    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
        // TODO add your handling code here:
        
         String strSearch = "select * from employee_data where ";
         boolean check = false ;
        if (txtsearch.getText().equals("")){
           Tools.msgBox("The Search Text is empty..."); 
        }
        else{
         if (rdoaddress.isSelected()){
             if (txtsearch.getText().matches("[a-zA-Z]+")){
                 strSearch+= "Address"+ getMyLike();
                 check = true;
             }
             else{
                 Tools.msgBox("The Search Text must be letters only...");
             }
         }
         else if (rdoname.isSelected()) {
             if (txtsearch.getText().matches("[a-zA-Z]+")){
                 strSearch+= "Name" + getMyLike();
                 check = true;
             }
             else{
                 Tools.msgBox("The Search Text must be letters only...");
             }
         }
         else if (rdonumber.isSelected()) {
             if (txtsearch.getText().matches("[0-9]+")){
                 strSearch+= "Number" + getMyLike();
                 check = true;
             }
             else{
                 Tools.msgBox("The Search Text must be numbers only...");
             }
         }
         else if (rdobirthdate.isSelected()) {
             if (txtsearch.getText().matches("[0-9|-]+")){
                 strSearch+= "Date_Of_Birth"+ getMyLike();
                 check = true;
             }
             else{
                 Tools.msgBox("The Search Text contain numbers Or special character (-) only...");
             }
         }
         else if (rdohiringdate.isSelected()) {
             if (txtsearch.getText().matches("[0-9|-]+")){
                 strSearch+= "Hiring_Date"+ getMyLike();
                 check = true;
             }
             else{
                 Tools.msgBox("The Search Text contain numbers Or special character (-) only...");
             }
         }
         else if (rdodept.isSelected()) {
             if (txtsearch.getText().matches("[0-9|a-zA-Z]+")){
                 strSearch+= "department_no"+ getMyLike() + " or department " + getMyLike();
                 check = true;
             }
             else{
                 Tools.msgBox("The Search Text contain numbers or letters only...");
             }
         }
         else if (rdosalary.isSelected()) {
             if (txtsearch.getText().matches("[0-9]+[.][0-9]+") || txtsearch.getText().matches("[0-9]+")){
                 strSearch+= "Salary"+ getMyLike();
                 check = true;
             }
             else{
                 Tools.msgBox("The Search Text contain positive real numbers only...");
             }
         }
         else  {
             if (txtsearch.getText().matches("[0][1][2|1|0|5]\\d{8}")){
                 String strEmpNum = Phones.getEmpNoByPhone(txtsearch.getText());
                 strSearch+= "Number like '%"+ strEmpNum + "%'";
                 check = true;
             }
             else{
                 Tools.msgBox("The Search Text invalid...");
             }
         }
         
         if (check == true){
              emp.getCustomRows(strSearch, tblEmp);  
             }

        }
    }//GEN-LAST:event_btnsearchActionPerformed

