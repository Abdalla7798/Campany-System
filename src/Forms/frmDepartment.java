    private void setValues(){
        dept.setDeptNo(Integer.parseInt(txtdeptno.getText()));
        dept.setDeptName(txtdeptname.getText());
        dept.setLocation(txtlocation.getText());
    }
    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        // TODO add your handling code here:
        if(txtdeptno.getText().equals("") || txtdeptname.getText().equals("") || txtlocation.getText().equals("")){
          Tools.msgBox("The information is not Completed...");
        }
        else{
            if (txtdeptno.getText().matches("[0-9]+")){
                if(txtdeptname.getText().matches("[a-zA-Z][0-9|a-zA-Z|\\s]+")){
                    if (txtlocation.getText().matches("[a-zA-Z]+")){
                    setValues();
                    dept.add();
                    clearData();
                    dept.getAllRows(tbldept);
                    }
                    else{
                      Tools.msgBox("Location must be Letters only...");  
                    }
                }
                else{
                 Tools.msgBox("Department Name must be numbers or Letters only but no number in the first digit...");   
                }
            }
            else{
                Tools.msgBox("Department Number must be numbers only...");
            }
        }
    }//GEN-LAST:event_btnaddActionPerformed

