    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
        String strSearch = "select * from department_data where ";
        boolean check = false ;

        if (txtsearch.getText().equals("")){
           Tools.msgBox("The Search Text is empty..."); 
        }
        else{           
             if (name.isSelected()){
                 if (txtsearch.getText().matches("[0-9|a-zA-Z]+")){
                     strSearch+= "department";
                     check = true;
                 }
                 else {
                    Tools.msgBox("The Search Text must contain letters or numbers only..."); 
                 }
             }
             else if (number.isSelected())  {
                 if (txtsearch.getText().matches("[0-9]+")){
                     strSearch+= "department_no";
                     check= true;
                 }
                 else {
                    Tools.msgBox("The Search Text must contain numbers only..."); 
                 }
             }
             else{
                 if (txtsearch.getText().matches("[a-zA-Z]+")){
                     strSearch+= "location";
                     check= true;
                 }
                 else {
                    Tools.msgBox("The Search Text must contain letters only...");
                 }
             }
             
             if (check == true){
                strSearch+=" like '%"+txtsearch.getText()+"%'";
                dept.getCustomRows(strSearch, tbldept); 
             }
        }    
    }//GEN-LAST:event_btnsearchActionPerformed

